package tugaspertemuan7.bilanganGanjil;



public class bilanganGanjil {
    public static void main(String[] args) {
    System.out.println("Deret Bilangan Ganjil dari 0 - 40");
    for (int i = 1; i <= 40; i += 2) {
      System.out.println(i);
    }

    System.out.println("\nDeret Bilangan Genap dari 0 - 40");
    for (int i = 0; i <= 40; i += 2) {
      System.out.println(i);
    }
  }
}
